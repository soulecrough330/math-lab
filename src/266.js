function getRandomInt(max) {
  return Math.floor(Math.random() * max);
}

// Example usage:
const num1 = getRandomInt(5); // Generates a random integer between 0 and 4 (inclusive)
console.log(num1);

num2 = getRandomInt(5); // Generates a random integer between 0 and 4 (exclusive)
console.log(num2);
