function sum(a, b) {
    console.log(`Adding ${a} + ${b}`);
    return a + b;
}

console.log(sum(3, 4)); // Output: Adding 3 + 4

function multiply(x, y) {
    let result = x * y;
    console.log(`Multiplying ${x} by ${y}:`, result);
    return result;
}

multiply(10, 7); // Output: Multiplying 10 by 7: 70
